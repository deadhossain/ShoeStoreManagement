    public void gettransid()//getting transaction id from database
    {
        try{
            int tranid;
    conn = DatabaseConnection.getConnection();
            
    String query =  "SELECT transactionid FROM transactionsummary ORDER BY transactionid DESC LIMIT 1";
    pst = conn.prepareStatement(query);
    rs = pst.executeQuery();
    if(rs.next())
    {
    tranid = Integer.parseInt(rs.getString("transactionid"));
    tranid++;
    }
    else{
    tranid=0;
    tranid++;
    }
    transactionid.setText(String.valueOf(tranid));
    }catch(Exception e)
    {
    JOptionPane.showMessageDialog(null,e);
    }    
    }
    
    
    
 
