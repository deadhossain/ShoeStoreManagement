    public boolean getrow(String code)
    {
    int rowcount = saleitemtb.getRowCount();
    String c;
    boolean b=false;
    for(int i=0;i<rowcount;i++)
    {
    c=saleitemtb.getValueAt(i,0).toString();
    if(c.equals(code))
    {
    b = true;
    break;
    }
    }
    return b;
    }
    
